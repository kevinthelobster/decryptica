def add_article_to_articles_ts(slug: str, title: str, excerpt: str, content: str,
                                 category: str, tags: list, faqs: list,
                                 date: str = None) -> bool:
    """
    Add a new article entry to the Next.js articles.ts data file.
    """
    if date is None:
        date = datetime.now().strftime("%Y-%m-%d")

    word_count = estimate_word_count(content)
    read_time = get_read_time(word_count)

    # Escape strings for TypeScript
    title_esc = title.replace("`", "\\`").replace("$", "\\$")
    excerpt_esc = excerpt.replace("`", "\\`").replace("$", "\\$")

    new_entry = f"""
  {{
    id: '{slug}',
    slug: '{slug}',
    title: `{title_esc}`,
    excerpt: `{excerpt_esc}`,
    content: `{content}`,
    category: '{category}' as const,
    readTime: '{read_time}',
    date: '{date}',
    author: 'Decryptica',
    tags: {json.dumps(tags)},
    wordCount: {word_count},
    faqs: {json.dumps(faqs, ensure_ascii=False)},
  }},"""

    if not os.path.exists(ARTICLES_TS_PATH):
        print(f"  ERROR: articles.ts not found at {ARTICLES_TS_PATH}")
        return False

    with open(ARTICLES_TS_PATH, "r") as f:
        content_ts = f.read()

    # Check if article already exists
    if f"id: '{slug}'" in content_ts or f'slug: "{slug}"' in content_ts:
        print(f"  Article '{slug}' already exists in articles.ts — skipping")
        return False

    # Find the end of the articles array and insert before the closing bracket
    # Look for the last ], in the file that's followed by ; or export
    match = re.search(r"(,\n\])", content_ts)
    if match:
        insert_pos = match.start() + 1  # After the comma, at the ]
        new_content = content_ts[:insert_pos] + new_entry + "\n" + content_ts[insert_pos:]
    else:
        # Fallback: look for export function
        print(f"  Warning: Could not find articles array end — appending at end of file")
        new_content = content_ts + new_entry

    with open(ARTICLES_TS_PATH, "w") as f:
        f.write(new_content)

    print(f"  ✓ Added '{slug}' to articles.ts ({word_count} words)")
    return True


