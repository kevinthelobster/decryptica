def slugify(text: str) -> str:
    """Convert text to URL-safe slug"""
    text = text.lower()
    text = re.sub(r"[^a-z0-9\s-]", "", text)
    text = re.sub(r"[\s]+", "-", text)
    text = re.sub(r"-+", "-", text)
    return text.strip("-")


def estimate_word_count(content: str) -> int:
    """Estimate word count from markdown content"""
    clean = re.sub(r"```[\s\S]*?```", "", content)  # Remove code blocks
    clean = re.sub(r"`[^`]+`", "", clean)            # Remove inline code
    clean = re.sub(r"\[([^\]]+)\]\([^)]+\)", r"\1", clean)  # Links → text
    clean = re.sub(r"[*_#>|]", "", clean)            # Remove markdown
    words = [w for w in clean.split() if len(w) > 1]
    return len(words)


def get_read_time(word_count: int) -> str:
    """Convert word count to read time string"""
    minutes = max(1, round(word_count / 200))
    return f"{minutes} min read"


